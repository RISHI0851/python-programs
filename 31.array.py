import numpy as np

# Sample array
data = np.array([10, 20, 30, 40, 50])

# Calculate mean
mean = np.mean(data)

# Calculate median
median = np.median(data)

# Calculate standard deviation
std_deviation = np.std(data)

# Print the results
print("Array:", data)
print("Mean:", mean)
print("Median:", median)
print("Standard Deviation:", std_deviation)
